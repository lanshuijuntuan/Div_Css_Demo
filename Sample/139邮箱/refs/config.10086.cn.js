//根据key获取queryStr中的vlaue
function getQueryStrVauleByKey(key) {

    if (!key) {
        return "";
    }

    var queryStr = location.search.substr(1);
    var keys     = queryStr.split("&");
    var keyValue = null,
        value    = "";

    for (var i = 0, n = keys.length; i < n; i++) {
        keyValue = keys[i].split("=");
        if (keyValue.length == 2 && keyValue[0] == key) {
            value = keyValue[1];
            break;
        }
    }

    return value;

}

function getCookie(name) {

    //k是为了解决多用户cookie冲突问题而加入到url的参数
    var k      = getQueryStrVauleByKey("k", location.search.substr(1));
    var arr    = null;
    var result = "";

    if (k) {
        arr = document.cookie.match(new RegExp("(^|\\W)" + name + k + "=([^;]*)(;|$)"));
        arr = !arr ? document.cookie.match(new RegExp("(^|\\W)" + name + "=([^;]*)(;|$)")) : arr;
    } else {
        arr = document.cookie.match(new RegExp("(^|\\W)" + name + "=([^;]*)(;|$)"));
    }

    if (arr != null) {
        result = unescape(arr[2]);
    }

    //当灰度中帐号，与全网帐号在相同浏览器下，依次登录，灰度帐号会读取到全网的cookiepartid，这段代码，则根据url中的appmail3进行分区修正。
    if (name == "cookiepartid" && /appmail[^\.]*?.mail.10086.cn/.test(location.host)) {
        if (window.PARTID) { //缓存partid
            return window.PARTID;
        }
        var betahost = domainList["global"]["betadomain"];//"appmail3.mail.10086.cn";
        if (betahost) { //有配置才修正
            if (result == "1" && location.host != betahost) {
                result = "12";
            } else if (result == "12" && location.host == betahost) {
                result = "1";
            }
        }
        window.PARTID = result;
    }

    return result;

}
